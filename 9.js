// Calculate the principal value of cos^{-1}(-1/2)
const principalValue = Math.acos(-1/2);

// Convert the result from radians to degrees
const principalValueInDegrees = principalValue * (180 / Math.PI);

// Print the result
console.log("Principal Value (in radians):", principalValue);
console.log("Principal Value (in degrees):", principalValueInDegrees);
